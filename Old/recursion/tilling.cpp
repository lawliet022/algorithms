template<typename T, typename... Args>
void err(istream_iterator<string> it, T a, Args... args) {
	cerr << "[" << *it << " = " << a << "]" << " ";
	err(++it, args...);
}


// Given a 2*n board count number of ways to fill it with 1*2 tiles

int solve(int n){
	if(n <= 1)
		return 1;
	return solve(n-1) + solve(n-2);
}


